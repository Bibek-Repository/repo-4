# maths in python

#some built in math functions

x=min(10,15,62)     # Returns the minimum of the value
y=max(15,34,81)     # Returns the maximum of the value
print(x,y)
print()

x=abs(-9.4572)      # Returns the absolute value with no sign
print(x)
print()

x=pow(3,2)          # Returns 3 raised to the power of 2
print(x)
print()

# The math module in python

import math

x=math.sqrt(4)      # Returns the square root of a number
print(int(x))
print()

x=math.ceil(1.5)        # Returns the integer value closest to the argument but greater than the argument
y=math.floor(1.5)       # Returns the integer value closest to the argument but less than the argument
print(x,y)
print()

x=math.pi               # Returns the value of pi
print(x)
print()



